    public void updateBookedRoomsTable(){
        this.RoomsBooked =  this.clientSocket.getBookedRooms();

        while(BookedRoomsTable.getRowCount()>0){
                ((DefaultTableModel) BookedRoomsTable.getModel()).removeRow(0);
            }

        DefaultTableModel model = (DefaultTableModel) BookedRoomsTable.getModel();
        
        
        for (BookRoom b: this.RoomsBooked){
            model.addRow(new Object[] { b.FirstName,b.LastName, b.Age, b.PhoneNumber,b.DateCheckedIn,b.RoomNo,b.RoomTypeName,b.RoomTypePrice});
        }
    }

